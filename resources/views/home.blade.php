                            <p>『您是我的守護天使』，這是在座每一位的心聲！</p>

                            <p>近日我想寫點東西懷緬我這位親愛的姐姐，但寫了這兩句就沒法寫下去了。二家姐對我們的愛護實在是太多太多，我們對她的回憶滿滿，實難以筆墨形容！</p>

                            <p>小時候我被狗咬傷，是她抱著我，陪著我看醫生；安安兒時住院，是她擁著我，陪我一起哭，一起心痛；安安托兒在保姆家，是她陪我一起去，還記得那天，她哭著緊緊的抱著安安，不捨得放下他的樣子。事後我倆回憶時都笑說，那時我們一副放低安安就不接回他的樣子，那姨姨當時會不會好驚！</p>

                            <p>太多太多溫馨的回憶足以令我懷念半生，我感恩我生命有您，正如詩歌唱出：全因為您，令我一生佳美！多謝您，我愛您！我永遠懷念您！</p>

                            <p>近日心靈極需要慰寂，時常聽敬拜歌曲，發現有好多首歌曲都讓我想起您，所以我斷定您就是神派給我的使者！感謝主！家姐，如今您的使命已經超額完成，返回天家與哥哥重聚，記望他日我們在天家再聚。</p>

                            <footer class="blockquote-footer">七妹</footer>
