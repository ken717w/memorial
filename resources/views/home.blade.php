                            <p>姨媽係張家靈魂人物，好照顧，好愛錫屋企每一位。姨媽係我童年時嘅一位重要女主角，我好記得細個時成日都跟住姨媽，佢經常帶我去玩，仲教識我游水，踩roller，打羽毛球，跟住姨媽好開心，佢比咗好多兒時嘅歡樂時光我。到我大個咗姨媽知我考到車牌，佢同我講，要多啲操吓手車，叫我隨時攞佢架車去揸吓，點知有次我撞爛咗架車,但係佢同姨丈都無怪責過我，諗返起呢件事，真係想再同佢哋講聲sorry。姨媽係我好尊敬嘅長輩，所以到我結婚時，我第一時間就諗要搵佢幫我上頭，好記得嗰日佢拎咗好多嘢嚟為我張羅，連床鋪都幫我換埋，又搵佢朋友幫我化妝set頭，出心出力。我真係好幸運有個咁好嘅姨媽，佢和藹可親嘅笑容，親切關懷嘅聲音，我永遠都唔會忘記。姨媽，好多謝你，我愛你！</p>

                            <footer class="blockquote-footer">姨甥女　Nicky 嘉碧</footer>
